#include "libft.h"

int	get_num_length(int n)
{
	int	count;

	count = 1;
	if (n < 0)
	{
		n *= -1;
		count++;
	}
	while (n / 10)
	{
		count++;
		n /= 10;
	}
	return (count);
}
